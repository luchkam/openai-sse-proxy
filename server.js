app.get('/ask', async (req, res) => {
  const userMessage = req.query.message;
  const threadId = req.query.thread_id;

  if (!threadId) {
    process.stdout.write('ÐžÑˆÐ¸Ð±ÐºÐ°: Ð¾Ñ‚ÑÑƒÑ‚ÑÑ‚Ð²ÑƒÐµÑ‚ thread_id\n');
    res.status(400).json({ error: 'thread_id Ð¾Ñ‚ÑÑƒÑ‚ÑÑ‚Ð²ÑƒÐµÑ‚' });
    return;
  }

  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('Connection', 'keep-alive');

  process.stdout.write(`Ð—Ð°Ð¿Ñ€Ð¾Ñ Ðº OpenAI Ñ thread_id: ${threadId}, ÑÐ¾Ð¾Ð±Ñ‰ÐµÐ½Ð¸Ðµ: ${userMessage}\n`);

  try {
    const run = await axios.post(
      `https://api.openai.com/v1/threads/${threadId}/runs`,
      {
        assistant_id: process.env.ASSISTANT_ID,
        stream: true,
        additional_messages: [
          {
            role: 'user',
            content: userMessage,
          },
        ],
      },
      {
        headers: {
          Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
          'OpenAI-Beta': 'assistants=v2',
        },
        responseType: 'stream',
      }
    );

    run.data.on('data', async (chunk) => {
      const lines = chunk.toString().split('\n');
      for (const line of lines) {
        if (!line.startsWith('data: ')) continue;

        const jsonStr = line.slice(6);
        if (jsonStr === '[DONE]') {
          res.write('data: [DONE]\n\n');
          res.end();
          process.stdout.write('ÐŸÐ¾Ñ‚Ð¾Ðº Ð·Ð°Ð²ÐµÑ€ÑˆÐµÐ½\n');
          return;
        }

        let data;
        try {
          data = JSON.parse(jsonStr);
        } catch (e) {
          continue;
        }

        // ðŸ›  ÐžÐ±Ñ€Ð°Ð±Ð¾Ñ‚ÐºÐ° Ð²Ñ‹Ð·Ð¾Ð²Ð° Ñ„ÑƒÐ½ÐºÑ†Ð¸Ð¸ get_weather
        if (data.event === 'thread.run.requires_action' && data.data?.required_action?.submit_tool_outputs) {
          const toolCalls = data.data.required_action.submit_tool_outputs.tool_calls;
          const outputs = [];

          for (const call of toolCalls) {
            if (call.function.name === 'get_weather') {
              const args = JSON.parse(call.function.arguments);
              process.stdout.write(`ðŸŒ ÐžÐ±Ñ€Ð°Ð±Ð¾Ñ‚ÐºÐ° get_weather Ð´Ð»Ñ: ${args.location} (${args.unit})\n`);
              const result = await getWeather(args.location, args.unit);
              outputs.push({ tool_call_id: call.id, output: result });
            }
          }

          await axios.post(
            `https://api.openai.com/v1/threads/${threadId}/runs/${data.data.id}/submit_tool_outputs`,
            { tool_outputs: outputs },
            {
              headers: {
                Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
                'OpenAI-Beta': 'assistants=v2',
              },
            }
          );
        }

        // ðŸ’¬ Ð¢ÐµÐºÑÑ‚Ð¾Ð²Ñ‹Ðµ Ð¾Ñ‚Ð²ÐµÑ‚Ñ‹
        if (data.event === 'thread.message.delta') {
          const content = data.delta?.content?.[0]?.text?.value;
          if (content) {
            res.write(`data: ${JSON.stringify({ content })}\n\n`);
            process.stdout.write(`ðŸ“¤ ${content}\n`);
          }
        }
      }
    });
  } catch (error) {
    process.stdout.write(`ÐžÑˆÐ¸Ð±ÐºÐ° Ð² /ask: ${error.message}\n`);
    res.write(`data: {"error":"${error.message}"}\n\n`);
    res.end();
  }
});

