app.get('/ask', async (req, res) => {
  const userMessage = req.query.message;
  const threadId = req.query.thread_id;

  if (!threadId) {
    process.stdout.write('–û—à–∏–±–∫–∞: –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç thread_id\n'); // –õ–æ–≥–∏—Ä—É–µ–º –æ—Ç—Å—É—Ç—Å—Ç–≤–∏–µ thread_id
    res.status(400).json({ error: 'thread_id –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç' });
    return;
  }

  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('Connection', 'keep-alive');

  process.stdout.write(`–ó–∞–ø—Ä–æ—Å –∫ OpenAI —Å thread_id: ${threadId}, —Å–æ–æ–±—â–µ–Ω–∏–µ: ${userMessage}\n`); // –õ–æ–≥–∏—Ä—É–µ–º –Ω–∞—á–∞–ª–æ –∑–∞–ø—Ä–æ—Å–∞

  try {
    const run = await axios.post(
      `https://api.openai.com/v1/threads/${threadId}/runs`,
      {
        assistant_id: process.env.ASSISTANT_ID,
        stream: true,
        additional_messages: [
          {
            role: 'user',
            content: userMessage,
          },
        ],
      },
      {
        headers: {
          Authorization: `Bearer ${process.env.OPENAI_API_KEY}`,
          'OpenAI-Beta': 'assistants=v2',
        },
        responseType: 'stream',
      }
    );

    run.data.on('data', (chunk) => {
      const lines = chunk.toString().split('\n');
      for (const line of lines) {
        if (line.startsWith('data: ')) {
          const jsonStr = line.slice(6);
          if (jsonStr !== '[DONE]') {
            res.write(`data: ${jsonStr}\n\n`);
            process.stdout.write(`–û—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ: ${jsonStr}\n`); // –õ–æ–≥–∏—Ä—É–µ–º –æ—Ç–ø—Ä–∞–≤–∫—É –¥–∞–Ω–Ω—ã—Ö
          }
        }
      }
    });

    run.data.on('end', () => {
      res.write('data: [DONE]\n\n');
      res.end();
      process.stdout.write('–ü–æ—Ç–æ–∫ –∑–∞–≤–µ—Ä—à–µ–Ω\n'); // –õ–æ–≥–∏—Ä—É–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏–µ –ø–æ—Ç–æ–∫–∞
    });

  } catch (error) {
    process.stdout.write(`–û—à–∏–±–∫–∞ –≤ /ask: ${error.message}\n`); // –õ–æ–≥–∏—Ä—É–µ–º –æ—à–∏–±–∫—É
    console.error('–û—à–∏–±–∫–∞ –≤ /ask:', error.message);
    res.write(`data: {"error":"${error.message}"}\n\n`);
    res.end();
  }
});

app.post('/search-tours', async (req, res) => {
  const { departureId, countryId, dateFrom, nights, adults, children, stars, mealCode } = req.body;

  // –õ–æ–≥–∏—Ä—É–µ–º –∑–∞–ø—Ä–æ—Å
  process.stdout.write(`üîç –ü–æ–∏—Å–∫ —Ç—É—Ä–æ–≤: ${JSON.stringify(req.body)}\n`);

  // 1. –ó–∞–ø—É—Å–∫ –ø–æ–∏—Å–∫–∞ —á–µ—Ä–µ–∑ Tourvisor API
  try {
    const searchParams = new URLSearchParams({
      authlogin: process.env.TOURVISOR_LOGIN,
      authpass: process.env.TOURVISOR_PASS,
      departure: departureId,
      country: countryId,
      datefrom: dateFrom,
      nightsto: nights,
      adults: adults,
      child: children || 0,
      stars: stars || 0,
      meal: mealCode || '',
      currency: 3, // –¢–µ–Ω–≥–µ
      format: 'json'
    });

    const searchUrl = `http://tourvisor.ru/xml/search.php?${searchParams}`;
    process.stdout.write(`üöÄ –ó–∞–ø—Ä–æ—Å –∫ Tourvisor: ${searchUrl}\n`);

    const searchResponse = await axios.get(searchUrl);
    const requestId = searchResponse.data.requestid;
    process.stdout.write(`üÜî ID –∑–∞–ø—Ä–æ—Å–∞ Tourvisor: ${requestId}\n`);

    // 2. –û–∂–∏–¥–∞–µ–º –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –ø–æ–∏—Å–∫–∞ (–ø—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å –∫–∞–∂–¥—ã–µ 2 —Å–µ–∫—É–Ω–¥—ã)
    let attempts = 0;
    let searchData;

    while (attempts < 5) {
      await new Promise(resolve => setTimeout(resolve, 2000));
      const statusUrl = `http://tourvisor.ru/xml/result.php?requestid=${requestId}&type=status&format=json`;
      const statusResponse = await axios.get(statusUrl);

      if (statusResponse.data.status.state === 'finished') {
        process.stdout.write(`‚úÖ –ü–æ–∏—Å–∫ –∑–∞–≤–µ—Ä—à–µ–Ω. –ù–∞–π–¥–µ–Ω–æ —Ç—É—Ä–æ–≤: ${statusResponse.data.status.toursfound}\n`);
        // 3. –ü–æ–ª—É—á–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
        const resultUrl = `http://tourvisor.ru/xml/result.php?requestid=${requestId}&type=result&format=json`;
        searchData = (await axios.get(resultUrl)).data;
        break;
      }
      attempts++;
    }

    if (!searchData) {
      throw new Error('–ü—Ä–µ–≤—ã—à–µ–Ω–æ –≤—Ä–µ–º—è –æ–∂–∏–¥–∞–Ω–∏—è –æ—Ç–≤–µ—Ç–∞ –æ—Ç Tourvisor');
    }

    // 4. –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Ç–æ–ø-3 —Ç—É—Ä–∞ –¥–ª—è Assistant
    const topTours = searchData.result.hotel.slice(0, 3).map(hotel => ({
      hotel: hotel.hotelname,
      price: hotel.price,
      nights: hotel.tours[0].nights,
      date: hotel.tours[0].flydate,
      meal: hotel.tours[0].mealrussian,
      operator: hotel.tours[0].operatorname
    }));

    res.json({ tours: topTours });

  } catch (error) {
    process.stdout.write(`‚ùå –û—à–∏–±–∫–∞: ${error.message}\n`);
    res.status(500).json({ error: '–ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ —Ç—É—Ä—ã' });
  }
});

